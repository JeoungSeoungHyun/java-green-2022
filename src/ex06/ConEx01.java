package ex06;

public class ConEx01 {
    public static void main(String[] args) {
        int point = 90; // 성적 (변수의 초기화)

        if (point >= 90) {
            System.out.println("A학점입니다");
        } else {
            System.out.println("A학점이 아닙니다");
        }
        // java에서 (){}의 형태에서 {}는 행위의 영역이다.
        // else는 if의 조건이 아니면 실행되므로 따로 조건이 필요없다

    }
}
